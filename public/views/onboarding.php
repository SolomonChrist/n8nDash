<h3>Welcome to n8nDash</h3>
<ol>
  <li>Import demo workflows from <code>demos/n8n/</code> into n8n.</li>
  <li>Run <code>SOURCE examples/seeds.sql;</code> in your MySQL to install sample dashboards.</li>
  <li>Edit each widget's <code>config_json.webhook_url</code> to your n8n webhook URL.</li>
  <li>Click <b>Main</b> to refresh data panels; click <b>Run</b> on app widgets.</li>
</ol>
